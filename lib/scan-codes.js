codes.getBreakCode = function (key) {
  var makeCode = codes[key];
  if (makeCode === undefined) {
    throw new Error("Undefined key: " + key);
  }

  if (key === "PAUSE") {
    return [];
  }

  if (makeCode[0] === 0xe0) {
    return [0xe0, makeCode[1] + 0x80];
  } else {
    return [makeCode[0] + 0x80];
  }
};

